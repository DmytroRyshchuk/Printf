#include "libft.h"

int		fill_by_zero(int n)
{
	int		i;

	i = 0;
	while (i < n)
	{
		write(1, "0", 1);
		i++;
	}
	return (i);
}

int		fill_by_empty(int n)
{
	int		i;

	i = 0;
	while (i < n)
	{
		write(1, " ", 1);
		i++;
	}
	return (i);
}
